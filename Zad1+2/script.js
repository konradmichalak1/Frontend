toggleMenu = function(){

    var hideMenu = true;
    var topValue = $('#main_navigation').position().top;
    $('.header-menu-list').on('click',function(){
        if(hideMenu === false){
            $('#main_navigation').animate({top: topValue},500);
            hideMenu = true;
        }else{
            $('#main_navigation').animate({top: 550},500);
            hideMenu = false;
        }
    })
}





